package JAVAPackage;

public class WhileVSDo_WhileLoop {

	public static void main(String[] args) {

		// While loop and do while loop comparison
		int i= 10;
		while(i<=50) {

			System.out.println(i);
			i+=10;
		}

		int a=5;
		do {
			System.out.println(a);
			a+=5;
		}while(a<=100);

	}

}
